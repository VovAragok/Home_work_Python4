col_bush = int(input("Введите количество кустов: "))
garden_bed = {}
sum_berry = 0

for i in range(1, col_bush + 1):
    garden_bed[i] = int(input("Введите число ягод на кусте: "))

harvest = int(input("Введите номер куста, с которого хотите собрать урожай: "))

if harvest == 1:
    # Собирают с первого куста
    sum_berry += garden_bed[harvest]  # Текущий куст
    sum_berry += garden_bed[harvest + 1]  # Второй куст
    sum_berry += garden_bed[col_bush]  # Последний куст
elif harvest == col_bush:
    # Собирают с последнего куста
    sum_berry += garden_bed[harvest]  # Текущий куст
    sum_berry += garden_bed[1]  # Первый куст
    sum_berry += garden_bed[harvest - 1]  # Предпоследний куст
else:
    # Собирают с остальных кустов
    sum_berry += garden_bed[harvest]  # Текущий куст
    sum_berry += garden_bed[harvest + 1]  # Следующий куст
    sum_berry += garden_bed[harvest - 1]  # Предыдущий куст

print(sum_berry)